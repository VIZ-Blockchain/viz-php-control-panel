function var_export_min($var,$return=false,$level=0,$prepend='') {
	if(is_array($var)){
		$toImplode=array();
		foreach($var as $key=>$value){
			$toImplode[]=var_export($key,true).'=>'.var_export_min($value,true,1+$level);
		}
		$code=$prepend.($level?'[':'').PHP_EOL.str_repeat("\t",$level).implode(','.PHP_EOL.str_repeat("\t",$level),$toImplode).PHP_EOL.str_repeat("\t",$level-1).($level?']':'').($prepend?';':'');
		if($return){
			return $code;
		}
		else{
			echo $code;
		}
	}
	else{
		return var_export($var,$return);
	}
}
function set_l10n($id){
	global $l10n,$l10n_default,$l10n_current,$l10n_presets,$l10n_preset;
	$err=false;
	if(is_int($id)){
		$code2=$l10n_base[$id]['code2'];
		if(isset($l10n_preset[$code2])){
			if($l10n_preset[$code2]['active']){
				$l10n=$l10n_preset[$code2];
				$l10n_current=$code2;
				return true;
			}
			else{
				$err=true;
			}
		}
		else{
			$err=true;
		}
	}
	else{
		if($l10n_presets[$id]){
			$code2=$l10n_presets[$id];
		}
		if(isset($l10n_preset[$code2])){
			$l10n=$l10n_preset[$code2];
			$l10n_current=$code2;
			return true;
		}
		else{
			$err=true;
		}
	}
	if($err){
		$l10n=$l10n_preset[$l10n_default];
		$l10n_current=$l10n_default;
		return false;
	}
}

